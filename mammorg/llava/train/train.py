def preprocess_mpt(
    sources,
    tokenizer: transformers.PreTrainedTokenizer,
) -> Dict:
    conv = conversation_lib.default_conversation.copy()
    roles = {"human": conv.roles[0], "gpt": conv.roles[1]}

    # Apply prompt templates
    conversations = []
    for i, source in enumerate(sources):
        if roles[source[0]["from"]] != conv.roles[0]:
            # Skip the first one if it is not from human
            source = source[1:]

        conv.messages = []
        for j, sentence in enumerate(source):
            role = roles[sentence["from"]]
            assert role == conv.roles[j % 2], f"{i}"
            conv.append_message(role, sentence["value"])
        conversations.append(conv.get_prompt())

    # Tokenize conversations
    input_ids = torch.stack([tokenizer_image_token(prompt, tokenizer, return_tensors='pt') for prompt in conversations], dim=0)
    targets = input_ids.clone()
    assert conv.sep_style == conversation_lib.SeparatorStyle.MPT

    # Mask targets
    sep = conv.sep + conv.roles[1]
    for conversation, target in zip(conversations, targets):
        total_len = int(target.ne(tokenizer.pad_token_id).sum())

        rounds = conversation.split(conv.sep)
        re_rounds = [conv.sep.join(rounds[:3])] # system + user + gpt
        for conv_idx in range(3, len(rounds), 2):
            re_rounds.append(conv.sep.join(rounds[conv_idx:conv_idx+2]))    # user + gpt
        cur_len = 0
        target[:cur_len] = IGNORE_INDEX
        for i, rou in enumerate(re_rounds):
            if rou == "":
                break

            parts = rou.split(sep)
            if len(parts) != 2:
                break
            parts[0] += sep
            round_len = len(tokenizer_image_token(rou, tokenizer)) + len(tokenizer_image_token(conv.sep, tokenizer))
            instruction_len = len(tokenizer_image_token(parts[0], tokenizer))
            target[cur_len : cur_len + instruction_len] = IGNORE_INDEX

            cur_len += round_len
        target[cur_len:] = IGNORE_INDEX

        if cur_len < tokenizer.model_max_length:
            if cur_len != total_len:
                target[:] = IGNORE_INDEX
                print(
                    f"WARNING: tokenization mismatch: {cur_len} vs. {total_len}."
                    f" (ignored)"
                )

    return dict(
        input_ids=input_ids,
        labels=targets,
    )


def preprocess_plain(
    sources: Sequence[str],
    tokenizer: transformers.PreTrainedTokenizer,
) -> Dict:
    # add end signal and concatenate together
    conversations = []
    for source in sources:
        assert len(source) == 2
        assert DEFAULT_IMAGE_TOKEN in source[0]['value']
        num_images = source[0]['value'].count(DEFAULT_IMAGE_TOKEN)
        source[0]['value'] = DEFAULT_IMAGE_TOKEN*num_images
        conversation = source[0]['value'] + source[1]['value'] + conversation_lib.default_conversation.sep
        conversations.append(conversation)
    # tokenize conversations
    input_ids = [tokenizer_image_token(prompt, tokenizer, return_tensors='pt') for prompt in conversations]
    targets = copy.deepcopy(input_ids)
    for target, source in zip(targets, sources):
        tokenized_len = len(tokenizer_image_token(source[0]['value'], tokenizer))
        target[:tokenized_len] = IGNORE_INDEX
    image_token_id = tokenizer.convert_tokens_to_ids("<image>")
    return dict(input_ids=input_ids, labels=targets)


def preprocess(
    sources: Sequence[str],
    tokenizer: transformers.PreTrainedTokenizer,
    has_image: bool = False
) -> Dict:
    """
    Given a list of sources, each is a conversation list. This transform:
    1. Add signal '### ' at the beginning each sentence, with end signal '\n';
    2. Concatenate conversations together;
    3. Tokenize the concatenated conversation;
    4. Make a deepcopy as the target. Mask human words with IGNORE_INDEX.
    """
    if conversation_lib.default_conversation.sep_style == conversation_lib.SeparatorStyle.PLAIN:
        return preprocess_plain(sources, tokenizer)
    if conversation_lib.default_conversation.sep_style == conversation_lib.SeparatorStyle.LLAMA_2:
        return preprocess_llama_2(sources, tokenizer, has_image=has_image)
    if conversation_lib.default_conversation.version.startswith("v1"):
        return preprocess_v1(sources, tokenizer, has_image=has_image)
    if conversation_lib.default_conversation.version == "mpt":
        return preprocess_mpt(sources, tokenizer)
    # add end signal and concatenate together
    conversations = []
    for source in sources:
        header = f"{conversation_lib.default_conversation.system}\n\n"
        conversation = _add_speaker_and_signal(header, source)
        conversations.append(conversation)
    # tokenize conversations
    def get_tokenize_len(prompts):
        return [len(tokenizer_image_token(prompt, tokenizer)) for prompt in prompts]

    if has_image:
        input_ids = [tokenizer_image_token(prompt, tokenizer, return_tensors='pt') for prompt in conversations]
    else:
        conversations_tokenized = _tokenize_fn(conversations, tokenizer)
        input_ids = conversations_tokenized["input_ids"]

    targets = copy.deepcopy(input_ids)
    for target, source in zip(targets, sources):
        if has_image:
            tokenized_lens = get_tokenize_len([header] + [s["value"] for s in source])
        else:
            tokenized_lens = _tokenize_fn([header] + [s["value"] for s in source], tokenizer)["input_ids_lens"]
        speakers = [sentence["from"] for sentence in source]
        _mask_targets(target, tokenized_lens, speakers)

    return dict(input_ids=input_ids, labels=targets)


class LazySupervisedDataset(Dataset):
    """Dataset for supervised fine-tuning."""

    def __init__(self, data_path: str,
                 tokenizer: transformers.PreTrainedTokenizer,
                 data_args: DataArguments):
        super(LazySupervisedDataset, self).__init__()
        list_data_dict = data_loaders[data_args.loader](data_path)

        rank0_print("Formatting inputs...Skip in lazy mode")
        self.tokenizer = tokenizer
        self.list_data_dict = list_data_dict
        self.data_args = data_args

    def __len__(self):
        return len(self.list_data_dict)

    @property
    def lengths(self):
        length_list = []
        for sample in self.list_data_dict:
            img_tokens = 256*4 if 'Image_paths' in sample else 0
            length_list.append(sum(len(conv['value'].split()) for conv in sample['conversations']) + img_tokens)
        return length_list

    @property
    def modality_lengths(self):
        length_list = []
        for sample in self.list_data_dict:
            cur_len = sum(len(conv['value'].split()) for conv in sample['conversations'])
            cur_len = cur_len if 'Image_paths' in sample else -cur_len
            length_list.append(cur_len)
        return length_list

    def __getitem__(self, i) -> Dict[str, torch.Tensor]:
        sources = self.list_data_dict[i]
        aux_labels=sources['aux_labels']
        if isinstance(i, int):
            sources = [sources]
        assert len(sources) == 1, "Don't know why it is wrapped to a list"  # FIXME
        if 'Image_paths' in sources[0]:
            image_file = self.list_data_dict[i]['Image_paths']
            image_folder = self.data_args.image_folder
            processor = self.data_args.image_processor
            images = {}
            
            for view in ['R_CC', 'R_MLO', 'L_CC', 'L_MLO']: 
                if view in image_file:
                    image = open_image_with_retry(os.path.join(image_folder, image_file[view]))
                    if image is None:
                        logging.error(f"Failed to load {view} image, using empty image.")
                        image = Image.new('RGB', (512, 512), tuple(int(x*255) for x in processor.image_mean))
                    if self.data_args.image_aspect_ratio == 'pad':
                        def expand2square(pil_img, background_color):
                            width, height = pil_img.size
                            if width == height:
                                return pil_img
                            elif width > height:
                                result = Image.new(pil_img.mode, (width, width), background_color)
                                result.paste(pil_img, (0, (width - height) // 2))
                                return result
                            else:
                                result = Image.new(pil_img.mode, (height, height), background_color)
                                result.paste(pil_img, ((height - width) // 2, 0))
                                return result
                        image = expand2square(image, tuple(int(x*255) for x in processor.image_mean))
                        image = processor.preprocess(image, return_tensors='pt')['pixel_values'][0]
                    else:
                        image = processor.preprocess(image, return_tensors='pt')['pixel_values'][0]
                    images[view]=image
            sources = preprocess_multimodal(
                copy.deepcopy([e["conversations"] for e in sources]),
                self.data_args)
        else:
            sources = copy.deepcopy([e["conversations"] for e in sources])
        data_dict = preprocess(
            sources,
            self.tokenizer,
            has_image=('Image_paths' in self.list_data_dict[i]))
        if isinstance(i, int):
            data_dict = data_dict = dict(
                input_ids=data_dict["input_ids"][0],  
                labels=data_dict["labels"][0],     
                aux_labels={
                    'left_density_logits': torch.tensor(aux_labels['left_density_logits'], dtype=torch.long),    
                    'left_birads_logits': torch.tensor(aux_labels['left_birads_logits'], dtype=torch.long),     
                    'left_status_logits': torch.tensor(aux_labels['left_status_logits'], dtype=torch.long), 
                    'right_density_logits': torch.tensor(aux_labels['right_density_logits'], dtype=torch.long),      
                    'right_birads_logits': torch.tensor(aux_labels['right_birads_logits'], dtype=torch.long),  
                    'right_status_logits': torch.tensor(aux_labels['right_status_logits'], dtype=torch.long), 
                    'located_at_logits': aux_labels['located_at_logits'],
                    'suggestive_of_logits': aux_labels['suggestive_of_logits'],
                    'modified_by_logits': aux_labels['modified_by_logits']
                },
                # relation_labels=relation_labels
            )

        # image exist in the data
        if 'Image_paths' in self.list_data_dict[i]:
            data_dict['image'] = images
        elif self.data_args.is_multimodal:
            # image does not exist in the data, but the model is multimodal
            crop_size = self.data_args.image_processor.crop_size
            for view in data_dict['image']:
                data_dict['image'][view] = torch.zeros(3, crop_size['height'], crop_size['width'])
        return data_dict


@dataclass
class DataCollatorForSupervisedDataset(object):
    """Collate examples for supervised fine-tuning."""

    tokenizer: transformers.PreTrainedTokenizer

    def __call__(self, instances: Sequence[Dict]) -> Dict[str, torch.Tensor]:
        input_ids, labels, aux_labels = tuple([instance[key] for instance in instances]
                                  for key in ("input_ids", "labels", "aux_labels"))
        input_ids = torch.nn.utils.rnn.pad_sequence(
            input_ids,
            batch_first=True,
            padding_value=self.tokenizer.pad_token_id)
        labels = torch.nn.utils.rnn.pad_sequence(labels,
                                                 batch_first=True,
                                                 padding_value=IGNORE_INDEX)
        input_ids = input_ids[:, :self.tokenizer.model_max_length]
        labels = labels[:, :self.tokenizer.model_max_length]
        aux_labels = {
            'left_density_logits': torch.cat([inst['aux_labels']['left_density_logits'] for inst in instances]), 
            'left_birads_logits': torch.cat([inst['aux_labels']['left_birads_logits'] for inst in instances]),
            'left_status_logits': torch.stack([
                inst['aux_labels']['left_status_logits'].view(-1) 
                for inst in instances
            ]), 
            'right_density_logits': torch.cat([inst['aux_labels']['right_density_logits'] for inst in instances]),
            'right_birads_logits': torch.cat([inst['aux_labels']['right_birads_logits'] for inst in instances]),
            'right_status_logits': torch.stack([
                inst['aux_labels']['right_status_logits'].view(-1)
                for inst in instances
            ]),
            'located_at_logits': torch.stack([
                inst['aux_labels']['located_at_logits'] 
                for inst in instances
            ]),
            'suggestive_of_logits': torch.stack([
                inst['aux_labels']['suggestive_of_logits']  
                for inst in instances
            ]),
            'modified_by_logits': torch.stack([
                inst['aux_labels']['modified_by_logits']
                for inst in instances
            ]),
        }
        batch = dict(
            input_ids=input_ids,
            labels=labels,
            attention_mask=input_ids.ne(self.tokenizer.pad_token_id),
            aux_labels=aux_labels,
        )

        if 'image' in instances[0]:
            images_list = [instance['image'] for instance in instances]
            if all(
                isinstance(x, dict) and 
                set(x.keys()) == {'R_CC', 'R_MLO', 'L_CC', 'L_MLO'} and
                all(
                    x[view].shape == images_list[0][view].shape 
                    for view in ['R_CC', 'R_MLO', 'L_CC', 'L_MLO']
                )
                for x in images_list
            ):
                # 如果所有图像形状一致，可以堆叠每个视图的图像
                batch_images = {
                    'R_CC': torch.stack([x['R_CC'] for x in images_list]),
                    'R_MLO': torch.stack([x['R_MLO'] for x in images_list]),
                    'L_CC': torch.stack([x['L_CC'] for x in images_list]),
                    'L_MLO': torch.stack([x['L_MLO'] for x in images_list])
                }
                batch['images'] = batch_images
            else:
                # 如果形状不一致，直接保留原始列表
                batch['images'] = images_list
        return batch


def make_supervised_data_module(tokenizer: transformers.PreTrainedTokenizer,
                                data_args) -> Dict:
    """Make dataset and collator for supervised fine-tuning."""
    train_dataset = LazySupervisedDataset(tokenizer=tokenizer,
                                data_path=data_args.data_path,
                                data_args=data_args)
    data_collator = DataCollatorForSupervisedDataset(tokenizer=tokenizer)
    return dict(train_dataset=train_dataset,
                eval_dataset=None,
                data_collator=data_collator)


